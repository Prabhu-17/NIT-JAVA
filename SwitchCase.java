class SwitchCase {
    public static void main(String[] args) {
        String s="Apple";
        switch (s) {
            case "Apple":
                System.out.println("Red color");
                break;
            case "Banana":
                System.out.println("Yellow color");
                break;
            case "Orange":
                System.out.println("Orange color ");
                break;
            default:
                System.out.println("Default case");
                break;
        }
    }
}
