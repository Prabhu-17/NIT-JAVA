class Operators {
    public static void main(String[] args) {
        int a='8';
            
        System.out.println(a);
        int i=5;
        System.out.println(8>>4);
        System.out.println(~5);
        System.out.println(5^7);
        System.out.println(5&7);
        System.out.println(5|7);
        i+=10;
        System.out.println(i>1 && i<10);
        System.out.println(i>1 || i<10);
        boolean flag=true;
        System.out.println(!flag);
        System.out.println(i++ + ++i);
        System.out.println(i-- - --i);

    }
}
